package ejerciciosArrays2;

public class Hotel {
    String descripcion;
    String nombre;
    String direccionPostal;
    int telefono;
    String email;

    public Hotel(String descripcion, String nombre, String direccionPostal, int telefono, String email){
        this.descripcion=descripcion;
        this.nombre=nombre;
        this.direccionPostal=direccionPostal;
        this.telefono=telefono;
        this.email=email;
    }
}
