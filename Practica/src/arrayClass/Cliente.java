package arrayClass;

public class Cliente {

    private static String nombre;
    private String dni;
    private String email;

    public String getNombre() {
        return nombre;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }



}
